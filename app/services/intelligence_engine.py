    def _check_position_concentration(self, company, amount: float) -> Optional[Warning]:
        """Check if this purchase would create concentration risk"""
        portfolio_value = self._get_portfolio_value()
        
        # Get existing position value
        existing_value = self._get_position_value(company.id)
        
        # Calculate new position percentage
        new_position_value = existing_value + amount
        new_portfolio_value = portfolio_value + amount
        
        if new_portfolio_value <= 0:
            return None
        
        new_position_pct = (new_position_value / new_portfolio_value) * 100
        threshold = self.THRESHOLDS['position_concentration_pct']
        
        if new_position_pct > threshold:
            # Determine severity based on how much over threshold
            if new_position_pct > threshold + 15:
                severity = 'high'
            elif new_position_pct > threshold + 5:
                severity = 'medium'
            else:
                severity = 'low'
            
            return Warning(
                code='position_concentration',
                severity=severity,
                category='concentration',
                title='Position Concentration Risk',
                message=f'This purchase would make {company.ticker_symbol} {new_position_pct:.1f}% of your portfolio (threshold: {threshold:.0f}%).',
                data={
                    'ticker': company.ticker_symbol,
                    'current_pct': (existing_value / portfolio_value * 100) if portfolio_value > 0 else 0,
                    'new_pct': new_position_pct,
                    'threshold': threshold,
                    'amount': amount
                }
            )
        
        return None
    
    def _check_sector_concentration(self, company, amount: float) -> Optional[Warning]:
        """Check if this purchase would create sector concentration"""
        if not company.sector:
            return None
        
        portfolio_value = self._get_portfolio_value()
        positions = self._get_portfolio_positions()
        
        sector_name = company.sector.display_name
        
        # Calculate current sector value
        current_sector_value = sum(
            float(p.current_value or 0)
            for p in positions
            if p.company and p.company.sector and p.company.sector.display_name == sector_name
        )
        
        # Calculate new sector percentage
        new_sector_value = current_sector_value + amount
        new_portfolio_value = portfolio_value + amount
        
        if new_portfolio_value <= 0:
            return None
        
        new_sector_pct = (new_sector_value / new_portfolio_value) * 100
        threshold = self.THRESHOLDS['sector_concentration_pct']
        
        if new_sector_pct > threshold:
            current_sector_pct = (current_sector_value / portfolio_value * 100) if portfolio_value > 0 else 0
            
            severity = 'high' if new_sector_pct > threshold + 10 else 'medium'
            
            return Warning(
                code='sector_concentration',
                severity=severity,
                category='concentration',
                title=f'{sector_name} Sector Concentration',
                message=f'This would bring {sector_name} to {new_sector_pct:.1f}% of your portfolio (threshold: {threshold:.0f}%).',
                data={
                    'sector': sector_name,
                    'current_pct': current_sector_pct,
                    'new_pct': new_sector_pct,
                    'threshold': threshold
                }
            )
        
        return None
    
