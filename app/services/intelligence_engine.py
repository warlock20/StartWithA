    def check_buy_transaction(
        self,
        company_id: int,
        amount: float,
        shares: Optional[float] = None,
        price_per_share: Optional[float] = None
    ) -> List[Warning]:
        """
        Check for warnings before a BUY transaction.
        
        Args:
            company_id: Company being purchased
            amount: Total dollar amount of purchase
            shares: Number of shares (optional)
            price_per_share: Price per share (optional)
            
        Returns:
            List of Warning objects
        """
        warnings = []
        
        company = Company.query.get(company_id)
        if not company:
            return warnings
        
        # Concentration checks
        if self.ENABLED_RULES.get('position_concentration'):
            warning = self._check_position_concentration(company, amount)
            if warning:
                warnings.append(warning)
        
        if self.ENABLED_RULES.get('sector_concentration'):
            warning = self._check_sector_concentration(company, amount)
            if warning:
                warnings.append(warning)
        
        if self.ENABLED_RULES.get('industry_concentration'):
            warning = self._check_industry_concentration(company, amount)
            if warning:
                warnings.append(warning)
        
        # Research checks
        if self.ENABLED_RULES.get('thesis_conflict'):
            warning = self._check_thesis_conflict(company)
            if warning:
                warnings.append(warning)
        
        if self.ENABLED_RULES.get('no_research'):
            warning = self._check_no_research(company)
            if warning:
                warnings.append(warning)
        
        # Behavioral checks - Buy side
        if self.ENABLED_RULES.get('buying_after_runup'):
            warning = self._check_buying_after_runup(company)
            if warning:
                warnings.append(warning)

        if self.ENABLED_RULES.get('chasing_returns'):
            warning = self._check_chasing_returns(company, amount)
            if warning:
                warnings.append(warning)
        
        if self.ENABLED_RULES.get('averaging_down'):
            warning = self._check_averaging_down(company, amount)
            if warning:
                warnings.append(warning)
        
        if self.ENABLED_RULES.get('overconfidence'):
            warning = self._check_overconfidence(company)
            if warning:
                warnings.append(warning)

        # Correlation check
        if self.ENABLED_RULES.get('high_correlation'):
            warning = self._check_high_correlation(company)
            if warning:
                warnings.append(warning)

        # Sort by severity
        severity_order = {'high': 0, 'medium': 1, 'low': 2, 'info': 3}
        warnings.sort(key=lambda w: severity_order.get(w.severity, 99))
        
        return warnings
    
    def get_portfolio_warnings(self) -> List[Warning]:
        """
        Get general portfolio-level warnings (not transaction-specific).
        
        Returns:
            List of Warning objects for portfolio health
        """
        warnings = []
        
        positions = self._get_portfolio_positions()
        portfolio_value = self._get_portfolio_value()
        
        if portfolio_value <= 0:
            return warnings
        
        # Check each position for concentration
        for position in positions:
            position_value = float(position.current_value or 0)
            position_pct = (position_value / portfolio_value) * 100
            
            if position_pct > self.THRESHOLDS['position_concentration_pct']:
                warnings.append(Warning(
                    code='existing_concentration',
                    severity='medium',
                    category='concentration',
                    title=f'{position.company.ticker_symbol} Concentration',
                    message=f'{position.company.ticker_symbol} is {position_pct:.1f}% of your portfolio. Consider rebalancing.',
                    data={
                        'ticker': position.company.ticker_symbol,
                        'percentage': position_pct,
                        'threshold': self.THRESHOLDS['position_concentration_pct']
                    }
                ))
        
        # Check sector concentration
        sector_totals = {}
        for position in positions:
            if position.company and position.company.sector:
                sector_name = position.company.sector.display_name
                sector_totals[sector_name] = sector_totals.get(sector_name, 0) + float(position.current_value or 0)
        
        for sector, total in sector_totals.items():
            sector_pct = (total / portfolio_value) * 100
            if sector_pct > self.THRESHOLDS['sector_concentration_pct']:
                warnings.append(Warning(
                    code='existing_sector_concentration',
                    severity='medium',
                    category='concentration',
                    title=f'{sector} Sector Heavy',
                    message=f'{sector} sector is {sector_pct:.1f}% of your portfolio.',
                    data={
                        'sector': sector,
                        'percentage': sector_pct,
                        'threshold': self.THRESHOLDS['sector_concentration_pct']
                    }
                ))
        
        return warnings
    
    def get_behavioral_insights(self) -> List[Warning]:
        """
        Get behavioral pattern insights for the user.
        
        Analyzes user's trading history to identify behavioral biases.
        Called from analytics/insights dashboard.
        
        Returns:
            List of behavioral insights/warnings
        """
        insights = []
        
        # Check overconfidence across all trades
        if self.ENABLED_RULES.get('overconfidence'):
            any_company = Company.query.filter_by(user_id=self.user_id).first()
            if any_company:
                warning = self._check_overconfidence(any_company)
                if warning:
                    insights.append(warning)
        
        # Check for losers being held too long
        if self.ENABLED_RULES.get('holding_loser_long'):
            positions = self._get_portfolio_positions()
            for position in positions:
                warning = self._check_holding_loser_long(position.company)
                if warning:
                    insights.append(warning)
        
        return insights
    
