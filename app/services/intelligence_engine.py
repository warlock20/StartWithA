@dataclass
class Warning:
    """A warning generated by the Intelligence Engine"""
    code: str                    # 'position_concentration', 'sector_overlap', etc.
    severity: str                # 'high', 'medium', 'low', 'info'
    category: str                # 'concentration', 'correlation', 'thesis', 'behavioral', 'research'
    title: str                   # Short title for display
    message: str                 # Detailed explanation
    data: Dict[str, Any] = field(default_factory=dict)  # Supporting data
    action_url: Optional[str] = None  # Link to take action
    dismissible: bool = True     # Can user dismiss this warning?


