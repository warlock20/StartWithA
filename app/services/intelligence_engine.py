    def _check_high_correlation(self, company) -> Optional[Warning]:
        """Check if new stock is highly correlated with existing positions"""
        positions = self._get_portfolio_positions()

        same_industry_positions = []
        same_sector_positions = []

        for p in positions:
            if not p.company or p.company_id == company.id:
                continue

            if company.industry and p.company.industry == company.industry:
                same_industry_positions.append(p)
            elif company.sector and p.company.sector and p.company.sector.id == company.sector.id:
                same_sector_positions.append(p)

        if len(same_industry_positions) >= 2:
            tickers = [p.company.ticker_symbol for p in same_industry_positions[:3]]
            industry_name = company.industry or 'same industry'

            return Warning(
                code='high_correlation_industry',
                severity='medium',
                category='correlation',
                title='Same Industry Concentration',
                message=f'{company.ticker_symbol} is in {industry_name}, where you already own {len(same_industry_positions)} positions: {", ".join(tickers)}. Industry-specific risks may be concentrated.',
                data={
                    'ticker': company.ticker_symbol,
                    'industry': industry_name,
                    'correlated_count': len(same_industry_positions),
                    'correlated_tickers': tickers
                }
            )

        if len(same_sector_positions) >= 3:
            tickers = [p.company.ticker_symbol for p in same_sector_positions[:3]]
            sector_name = company.sector.display_name if company.sector else 'same sector'

            return Warning(
                code='high_correlation_sector',
                severity='low',
                category='correlation',
                title='Sector Overlap',
                message=f'{company.ticker_symbol} is in {sector_name}, where you already own {len(same_sector_positions)} positions. These stocks may be correlated.',
                data={
                    'ticker': company.ticker_symbol,
                    'sector': sector_name,
                    'correlated_count': len(same_sector_positions),
                    'correlated_tickers': [p.company.ticker_symbol for p in same_sector_positions]
                }
            )

        return None
    
    # ═══════════════════════════════════════════════════════════════
    # HELPER METHODS
    # ═══════════════════════════════════════════════════════════════
    
    def _get_portfolio_positions(self):
        """Get all active portfolio positions (cached with eager loading)"""
        if self._portfolio_cache is None:
            self._portfolio_cache = PortfolioPosition.query.filter_by(
                user_id=self.user_id,
                is_active=True
            ).options(
                joinedload(PortfolioPosition.company).joinedload(Company.sector)
            ).all()
        return self._portfolio_cache
    
    def _get_portfolio_value(self) -> float:
        """Get total portfolio value (cached)"""
        if self._portfolio_value_cache is None:
            positions = self._get_portfolio_positions()
            self._portfolio_value_cache = sum(
                float(p.current_value or 0) for p in positions
            )
        return self._portfolio_value_cache
    
    def _get_position_value(self, company_id: int) -> float:
        """Get current value of a specific position"""
        positions = self._get_portfolio_positions()
        position = next((p for p in positions if p.company_id == company_id), None)
        return float(position.current_value or 0) if position else 0.0
    
    def clear_cache(self):
        """Clear cached data (call after portfolio changes)"""
        self._portfolio_cache = None
        self._portfolio_value_cache = None


