    def _check_thesis_conflict(self, company) -> Optional[Warning]:
        """Check if new purchase conflicts with existing thesis"""
        
        # Get existing positions and their theses
        positions = self._get_portfolio_positions()
        
        # Get the thesis for the company being purchased
        new_thesis_journal = DecisionJournal.query.filter_by(
            user_id=self.user_id,
            company_id=company.id,
            decision_type='BUY',
            is_portfolio_decision=True
        ).order_by(DecisionJournal.created_at.desc()).first()
        
        if not new_thesis_journal or not new_thesis_journal.investment_thesis:
            return None
        
        new_thesis = new_thesis_journal.investment_thesis.lower()
        
        # Simple conflict detection based on keywords
        # (In future, this could use Claude API for semantic analysis)
        
        conflicts = []
        
        for position in positions:
            if position.company_id == company.id:
                continue  # Skip same company
            
            # Get thesis for existing position
            existing_journal = DecisionJournal.query.filter_by(
                user_id=self.user_id,
                company_id=position.company_id,
                decision_type='BUY',
                is_portfolio_decision=True
            ).order_by(DecisionJournal.created_at.desc()).first()
            
            if not existing_journal or not existing_journal.investment_thesis:
                continue
            
            existing_thesis = existing_journal.investment_thesis.lower()
            
            # Check for potential conflicts (simplified keyword matching)
            # Example: Bullish on NVDA for "AI growth" but bearish on INTC thesis saying "AI commoditized"
            conflict_pairs = [
                (['bull', 'growth', 'increase', 'rise'], ['bear', 'decline', 'decrease', 'fall']),
                (['premium', 'pricing power'], ['commodity', 'race to bottom']),
                (['market leader', 'dominant'], ['losing share', 'disrupted']),
            ]
            
            for bullish_terms, bearish_terms in conflict_pairs:
                new_has_bullish = any(term in new_thesis for term in bullish_terms)
                existing_has_bearish = any(term in existing_thesis for term in bearish_terms)
                
                # Check if they're in same sector/industry (conflict more relevant)
                same_sector = (
                    company.sector and position.company.sector and
                    company.sector.id == position.company.sector.id
                )
                
                if new_has_bullish and existing_has_bearish and same_sector:
                    conflicts.append({
                        'ticker': position.company.ticker_symbol,
                        'thesis_snippet': existing_journal.investment_thesis[:100]
                    })
                    break
        
        if conflicts:
            return Warning(
                code='thesis_conflict',
                severity='medium',
                category='thesis',
                title='Potential Thesis Conflict',
                message=f'Your thesis for {company.ticker_symbol} may conflict with your thesis for {conflicts[0]["ticker"]}. Review both positions.',
                data={
                    'new_company': company.ticker_symbol,
                    'conflicting_positions': conflicts
                }
            )
        
        return None
    
    def _check_no_research(self, company) -> Optional[Warning]:
        """Check if buying without platform research"""
        if not self.THRESHOLDS.get('no_research_warning', True):
            return None
        
        # Check for completed research project
        research_project = ResearchProject.query.filter_by(
            user_id=self.user_id,
            company_id=company.id,
            status='completed'
        ).first()
        
        if research_project:
            return None
        
        # Check for completed checklist analysis
        checklist_analysis = ChecklistAnalysis.query.filter_by(
            user_id=self.user_id,
            company_id=company.id,
            status='completed'
        ).first()
        
        if checklist_analysis:
            return None
        
        # Check for active research
        active_research = ResearchProject.query.filter_by(
            user_id=self.user_id,
            company_id=company.id,
            status='active'
        ).first()
        
        if active_research:
            return Warning(
                code='research_incomplete',
                severity='medium',
                category='research',
                title='Research In Progress',
                message=f'You have active research on {company.ticker_symbol} that isn\'t complete. Consider finishing before buying.',
                data={
                    'ticker': company.ticker_symbol,
                    'research_id': active_research.id,
                    'progress': active_research.progress_percentage
                },
                action_url=f'/research/project/{active_research.id}'
            )
        
        # No research at all
